def wind_string(speed: float) -> str:
    if speed >= 0 and speed < 0.21: return 'Штиль'
    elif speed >= 0.3 and speed < 1.51: return 'Тихий'
    elif speed >= 1.6 and speed < 3.31: return 'Лёгкий'
    elif speed >= 3.4 and speed < 5.41: return 'Слабый'
    elif speed >= 5.5 and speed < 7.91: return 'Умеренный'
    elif speed >= 8.0 and speed < 10.71: return 'Свежий'
    elif speed >= 10.8 and speed < 13.81: return 'Сильный'
    elif speed >= 13.9 and speed < 17.11: return 'Крепкий'
    elif speed >= 17.2 and speed < 20.71: return 'Очень крепкий'
    elif speed >= 20.8 and speed < 24.41: return 'Шторм'
    elif speed >= 24.5 and speed < 28.41: return 'Сильный шторм'
    elif speed >= 28.5 and speed < 32.61: return 'Жестокий шторм'
    elif speed >= 33: return 'Ураган'

def wind_direction(deg: float) -> str:
    if deg >= 0.00 and deg <= 44.99: return 'северый'
    if deg >= 45.00 and deg <= 89.99: return 'северо-восточный'
    elif deg >= 90.00 and deg <= 134.99: return 'восточный'
    elif deg >= 135.00 and deg <= 179.99: return 'юго-восточный'
    elif deg >= 180.00 and deg <= 224.99: return 'югжный'
    elif deg >= 225.00 and deg <= 269.99: return 'юго-западый'
    elif deg >= 270.00 and deg <= 314.99: return 'западый'
    elif deg >= 315.00: return 'северо-западый'